<?php

namespace App\Http\Controllers;

use JWTAuth;
use App\User;
use Illuminate\Http\Request;
use Tymon\JWTAuth\Exceptions\JWTException;
use Symfony\Component\HttpFoundation\Response;
use Illuminate\Support\Facades\Validator;
use Log;

class ApiController extends Controller
{
    /**
    * Register
    * @param Request $request
    * @bodyParam name required Example: admin
    * @bodyParam email email required Example: test@gmail.com
    * @bodyParam password required Example: 123456
    * @return \Illuminate\Http\Response
    * @response {
    *    }
    */
    public function register(Request $request)
    {
    	//Validate data
        $data = $request->only('name', 'email', 'password');
        $validator = Validator::make($data, [
            'name' => 'required|string',
            'email' => 'required|email|unique:users',
            'password' => 'required|string|min:6|max:50'
        ]);

        //Send failed response if request is not valid
        if ($validator->fails()) {
            return response()->json(['error' => $validator->messages()], 200);
        }
        //how to do for loop in

        //Request is valid, create new user
        $user = User::create([
        	'name' => $request->name,
        	'email' => $request->email,
        	'password' => bcrypt($request->password)
        ]);
        Log::channel('slackInfo')->info('new User');

        //User created, return success response
        return response()->json([
            'success' => true,
            'message' => 'User created successfully',
            'data' => $user
        ], Response::HTTP_OK);
    }
    /**
    * Authenticate
    * @param Request $request
    * @bodyParam email email required Example: test@gmail.com
    * @bodyParam password string required Example: 123456
    * @return \Illuminate\Http\Response
    * @response {
    *    }
    */
    public function authenticate(Request $request)
    {
        $credentials = $request->only('email', 'password');
        Log::channel('slackCritical')->critical('into function autenticate');
        Log::channel('slackInfo')->info('into function autenticate');
        Log::channel('slackDebug')->debug('into function autenticate');
        Log::channel('slackEmergency')->emergency('into function autenticate');
        //valid credential
        $validator = Validator::make($credentials, [
            'email' => 'required|email',
            'password' => 'required|string|min:6|max:50'
        ]);

        //Send failed response if request is not valid
        if ($validator->fails()) {
            return response()->json(['error' => $validator->messages()], 200);
        }

        //Request is validated
        //Crean token
        try {
            if (! $token = JWTAuth::attempt($credentials)) {
                return response()->json([
                	'success' => false,
                	'message' => 'Login credentials are invalid.',
                ], 400);
            }
        } catch (JWTException $e) {
    	return $credentials;
            return response()->json([
                	'success' => false,
                	'message' => 'Could not create token.',
                ], 500);
        }
 	
 		//Token created, return with success response and jwt token
        return response()->json([
            'success' => true,
            'token' => $token,
        ]);
    }
    /**
    * Logout
    * @param Request $request
    * @bodyParam token string required
    * @return \Illuminate\Http\Response
    * @response {
    *    }
    */
    public function logout(Request $request)
    {
        //valid credential
        $validator = Validator::make($request->only('token'), [
            'token' => 'required'
        ]);

        //Send failed response if request is not valid
        if ($validator->fails()) {
            return response()->json(['error' => $validator->messages()], 200);
        }

		//Request is validated, do logout        
        try {
            JWTAuth::invalidate($request->token);
 
            return response()->json([
                'success' => true,
                'message' => 'User has been logged out'
            ]);
        } catch (JWTException $exception) {
            return response()->json([
                'success' => false,
                'message' => 'Sorry, user cannot be logged out'
            ], Response::HTTP_INTERNAL_SERVER_ERROR);
        }
    }
    /**
    * get User
    * @param Request $request
    * @bodyParam token string required
    * @return \Illuminate\Http\Response
    * @response {
    *    }
    */
    public function get_user(Request $request)
    {
        $this->validate($request, [
            'token' => 'required'
        ]);
 
        $user = JWTAuth::authenticate($request->token);
 
        return response()->json(['user' => $user]);
    }
}